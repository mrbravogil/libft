#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	char	*tmp_ptr;
	size_t	i;

	tmp_ptr = (char *)s;
	i = 0;
	while (n > 0)
	{
		tmp_ptr[i] = '\0';
		i++;
		n--;
	}
}
